package jp.sample2;

public class Sample2_3 {
	public static void main(String[] args) {
		int a; //変数の宣言
		a = 100; //aに100を代入
		int b = 300; //変数の宣言&bに300を代入
		
		System.out.println(a); //変数の利用(aを出力)
		System.out.println(b); //変数の利用(bを出力)
		
		a = 200; //aに200を代入
		System.out.println(a); //変数の利用(aを出力)
	}
}