package jp.ssie.ocjp;

public class Sample2_7 {
	public static void main(String[] args) {
	    int a = 10;
	    int b = 2;

	    System.out.println("a + b = " + (a + b));
	    System.out.println("a - b = " + (a - b));
	    System.out.println("a * b = " + (a * b)); 
	    System.out.println("a / b = " + (a / b)); //INT型で割った場合は整数のみ出力される(余りは切り捨て)
	    System.out.println("a % b = " + (a % b));
	    System.out.println("-a    = " + (-a));

	}

}